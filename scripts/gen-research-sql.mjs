/**
 * Generates supabase/research_list.sql (DDL + seed) from src/data/researchList.js
 * so the offline fallback dataset and the live table never drift apart.
 * Run: node scripts/gen-research-sql.mjs
 */
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import { researchList } from '../src/data/researchList.js';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');

const esc = (v) => (v === null || v === undefined ? 'NULL' : `'${String(v).replace(/'/g, "''")}'`);

const COLS = [
  'code', 'research_unit', 'task_name', 'status', 'start_year', 'end_year',
  'task_type', 'ordered_by', 'coordinator_manager', 'members', 'report_plan_link',
  'framework_transition', 'glocal_design', 'human_centric_orientation',
  'tech_solutions', 'urban_system',
];

const ddl = `-- ============================================================================
-- CL3. RESEARCH HUB — iscm_research_list (Supabase DDL)
-- Official database model mapped from "2026 ISCM - RESEARCH - Research List.tsv"
-- Drives the table view inside the Research List page (CL3 Research Sub-Workspace).
-- Generated by scripts/gen-research-sql.mjs — edit src/data/researchList.js instead.
-- ============================================================================

-- Create the main research activity tracking table
CREATE TABLE iscm_research_list (
    id SERIAL PRIMARY KEY,
    code VARCHAR(50) NULL,
    research_unit VARCHAR(150) NOT NULL,
    task_name TEXT NOT NULL,
    status VARCHAR(100) DEFAULT 'Not start',
    start_year VARCHAR(10) NULL,
    end_year VARCHAR(10) NULL,
    task_type VARCHAR(100) NULL,
    ordered_by VARCHAR(100) NULL,
    coordinator_manager VARCHAR(150) NULL,
    members TEXT NULL,
    report_plan_link TEXT NULL,
    framework_transition VARCHAR(100) NULL,
    glocal_design VARCHAR(100) NULL,
    human_centric_orientation VARCHAR(100) NULL,
    tech_solutions VARCHAR(100) NULL,
    urban_system VARCHAR(100) NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

-- Enable Row Level Security (RLS) for privacy settings control
ALTER TABLE iscm_research_list ENABLE ROW LEVEL SECURITY;

-- Director and Authorized Staff Global Access Policy
CREATE POLICY "Allow authenticated read access"
ON iscm_research_list FOR SELECT
TO authenticated
USING (true);
`;

const rows = researchList
  .map((r) => `  (${COLS.map((c) => esc(r[c])).join(', ')})`)
  .join(',\n');

const seed = `
-- ----------------------------------------------------------------------------
-- SEED — representative subset of the 2026 Research List TSV (all 10 units).
-- Import the full sheet later via CSV import against the same columns.
-- ----------------------------------------------------------------------------
INSERT INTO iscm_research_list
  (${COLS.join(', ')})
VALUES
${rows};
`;

writeFileSync(join(root, 'supabase', 'research_list.sql'), ddl + seed, 'utf8');
console.log(`Wrote supabase/research_list.sql with ${researchList.length} seed rows.`);
